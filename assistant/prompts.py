"""Системный промпт и сборка контекста для grounded-ответа.

Guardrails юридического продукта: отвечать ТОЛЬКО по переданным статьям,
цитировать, не выдумывать, дисклеймер про не-консультацию.
"""

SYSTEM_PROMPT = (
    "Ты — справочный ассистент по трудовому праву Российской Федерации. "
    "Отвечай СТРОГО на основе приведённых ниже статей нормативных актов. "
    "Если в приведённых статьях нет ответа — прямо скажи, что в доступном корпусе "
    "ответа нет, и не придумывай нормы. Каждое утверждение сопровождай ссылкой на "
    "конкретную статью в формате «Статья N». Это справочная информация, а не "
    "юридическая консультация. Отвечай по-русски, кратко и по делу."
)


def build_user_content(question, articles):
    """Собирает пользовательское сообщение: вопрос + пронумерованные статьи."""
    blocks = []
    for i, a in enumerate(articles, 1):
        label = a.article_label or "Статья"
        blocks.append(f"[{i}] {a.document_title} — {label}\n{a.text}")
    context = "\n\n".join(blocks)
    return (
        f"Вопрос: {question}\n\n"
        f"Статьи из корпуса:\n\n{context}\n\n"
        "Ответь на вопрос, опираясь только на эти статьи, со ссылками на номера статей."
    )
